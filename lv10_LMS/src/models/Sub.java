package models;

public class Sub {
	private String title;
	private int score;
	private int subCode;
	
	public String getTitle() {
		return this.title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
	public int getScore() {
		return this.score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public int getSubCode() {
		return this.subCode;
	}
	public void setSubCode(int subCode) {
		this.subCode = subCode;
	}

}
